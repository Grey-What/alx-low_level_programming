#include "main.h"
/**
 * add - adds two integers
 *
 * @a: first value
 * @b: second value
 *
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 *
 * @a: first value
 * @b: second value
 *
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply two integers
 *
 * @a: first value
 * @b: second value
 *
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two integers
 *
 * @a: first value
 * @b: second value
 *
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus two integers
 *
 * @a: first value
 * @b: second value
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
